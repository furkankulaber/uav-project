<script setup lang="ts">
import FullCalendar from '@fullcalendar/vue3'
import { useStore } from 'vuex'
import { blankEvent, useCalendar } from '@/views/apps/calendar/useCalendar'
import { useResponsiveLeftSidebar } from '@core/composable/useResponsiveSidebar'

// Components
import CalendarEventHandler from '@/views/apps/calendar/CalendarEventHandler.vue'

interface Props {
  id: string | number
}
const props = defineProps<Props>()

// ðŸ‘‰ Store
const store = useStore()
const isLoading = computed(() => store.state.loadingStore.isLoading)

watch(() => store.state.calendarStore.dateFilter, () => {
  store.dispatch('calendarStore/fetchEvents', { id: props.id })
}, { deep: true })

// ðŸ‘‰ Event
const event = ref(structuredClone(blankEvent))
const isEventHandlerSidebarActive = ref(false)

watch(isEventHandlerSidebarActive, val => {
  if (!val)
    event.value = structuredClone(blankEvent)
})

const { isLeftSidebarOpen } = useResponsiveLeftSidebar()

// ðŸ‘‰ useCalendar
const { refCalendar, calendarOptions, addEvent, updateEvent, removeEvent, jumpToDate, refetchEvents } = useCalendar(event, isEventHandlerSidebarActive, isLeftSidebarOpen, Number(props.id))
</script>

<template>
  <div>
    <VCard>
      <!-- `z-index: 0` Allows overlapping vertical nav on calendar -->
      <VLayout style="z-index: 0;">
        <VMain>
          <VCard flat>
            <FullCalendar
              ref="refCalendar"
              :options="calendarOptions"
            />
          </VCard>
        </VMain>
      </VLayout>
    </VCard>
    <CalendarEventHandler
      v-model:isDrawerOpen="isEventHandlerSidebarActive"
      :event="event"
      @add-event="addEvent"
      @update-event="updateEvent"
      @remove-event="removeEvent"
    />
  </div>
</template>

